# Entrada de dados
temperatura_c = float(input("Informe  a temperatura em graus Fahrenheit (Fº): "))

# Processamento de dados
fahrenheit = (temperatura_c * 9) / 5 + 32

# Saída de dados
print(f"A temperatura de {temperatura_c:.2f} (Cº) Celsius corresponde à {fahrenheit:.2f} (Cº) em graus Fahrenheit.")