def velocidade_media(distancia, tempo):
    velocidade = distancia / tempo
    return velocidade

print("A velicidade média é {}".format (velocidade_media(100,20)))
print("A velicidade média é {}".format (velocidade_media(150,22)))
print("A velicidade média é {}".format (velocidade_media(200,30)))
print("A velicidade média é {}".format (velocidade_media(50,3)))