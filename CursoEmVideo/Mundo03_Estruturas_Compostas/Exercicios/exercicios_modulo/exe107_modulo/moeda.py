# Módulos e Pacotes - Exercício: Exercitando módulos em python

def metade(preco):
    resultado = preco / 2
    return resultado

def dobro(preco):
    resultado = preco * 2
    return resultado


def aumentar(preco, taxa=0):
    resultado = preco + (preco * taxa/100)
    return resultado


def diminuir(preco, taxa=0):
    resultado = preco - (preco * taxa/100)
    return resultado




