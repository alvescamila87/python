#lista dentro de listas

carro_a = ["BMW", 180000]
carro_b = ["Polo", 75000]
carro_c = ["Jetta", 125000]

carros = [carro_a, carro_b, carro_c]

print(carros)

#acessar o primeiro veículo, da primeira lista
print(carros[0][0])

#acessar o valor do primeiro veículo, da primeira lista
print(carros[0][1])

#acessar o útlimo veículo, da última lista
print(carros[2][0])